package com.infinote.differentthinking.infinote.models;

public class Note {
    private String id;
    private String owner;

    public String getId() {
        return id;
    }

    public String getOwner() {
        return owner;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }
}
